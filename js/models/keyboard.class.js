
/**
* Tracks the current state of keyboard keys used for player input.
*/
class Keyboard {

    /** @type {boolean} - Left arrow key pressed */
    LEFT = false;

    /** @type {boolean} - Right arrow key pressed */
    RIGHT = false;

    /** @type {boolean} - Up arrow key pressed */
    UP = false;

    /** @type {boolean} - Down arrow key pressed */
    DOWN = false;

    /** @type {boolean} - Space key pressed (e.g., for jump or action) */
    SPACE = false;

    /** @type {boolean} - 'D' key pressed (e.g., for throwing or special action) */
    D = false;
}